    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed
        Aluno aluno = null;
        if (jRadioButtonRa.isSelected()) {
            // pedir para o usuario digitar o ra a ser pesquisado
            String ra = javax.swing.JOptionPane.showInputDialog(null, "Digite o RA do aluno: ");

            // declaração de uma variavel tipo Aluno e inicialização com os dados do aluno pesquisado
            aluno = alunosController.pesquisarPeloRa(ra);

        }
        if (jRadioButtonNome.isSelected()) {
            // pedir para o usuario digitar o ra a ser pesquisado
            String nome = javax.swing.JOptionPane.showInputDialog(null, "Digite o Nome do aluno: ");

            // declaração de uma variavel tipo Aluno e inicialização com os dados do aluno pesquisado
            aluno = alunosController.pesquisarpeloNome(nome);
        }

        if (aluno != null) {
            // chamada do metodo para carregar o formulario
            carregar(aluno);
            // habilitar o formulário
            habilitar();
            // tornar o campo de texto do RA não editável
            jTextFieldRa.setEditable(false);
            // habilitar botões
            jButtonAlterar.setEnabled(true);
            jButtonExcluir.setEnabled(true);
            jButtonCancelar.setEnabled(true);
            // desabilitar botões
            jButtonNovo.setEnabled(false);
            jButtonPesquisar.setEnabled(false);
            // apresenta mensagem de sucesso
            javax.swing.JOptionPane.showMessageDialog(null,
                    "Aluno encontrado!", getTitle(),
                    javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Aluno não encontrado!", getTitle(),
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

