    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        Aluno aluno = obterDados();
        // pedir para o usuário confirmar a exclusão
        int resposta = javax.swing.JOptionPane.showConfirmDialog(null,
                "Deseja excluir os dados do aluno?");
        if (resposta == 0) {
            if (alunosController.excluir(aluno)) {
                javax.swing.JOptionPane.showMessageDialog(null,
                        "Dados excluídos com sucesso!\n" + aluno, getTitle(),
                        javax.swing.JOptionPane.INFORMATION_MESSAGE);
            } else {
                javax.swing.JOptionPane.showMessageDialog(null,
                        "Erro: dados não excluídos!", getTitle(),
                        javax.swing.JOptionPane.ERROR_MESSAGE);
            }
            // desabilitar o formulário
            desabilitar();
            // habilitar botões
            jButtonNovo.setEnabled(true);
            jButtonPesquisar.setEnabled(true);
            // desabilitar botões
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            jButtonAlterar.setEnabled(false);
            jButtonExcluir.setEnabled(false);
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

